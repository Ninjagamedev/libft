#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	valor;
	int	sign;

	i = 0;
	valor = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
			valor = valor * 10 + ((int)str[i]) - '0';
		else
			return (sign * valor);
		i++;
	}
	return (sign * valor);
}
